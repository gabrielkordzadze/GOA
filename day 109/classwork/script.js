// ელემენტი გამრავლებული თავისივე ინდექსზე
var numbers = [3, 5, 7, 2, 9, 4, 6, 8];

function multiplyByIndex(value, index) {
    return value * index;
}

var result = numbers.map(multiplyByIndex);
console.log(result);


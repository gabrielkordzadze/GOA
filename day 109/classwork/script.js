//  და დააბრუნეთ კენტ ინდექსზე მდგომი ელემენტების ჯამი 

// 4) შექმენით სახელების სია (შეგიძლიათ გამოიყენოთ წინა დავალებაშ შექმნილი სიაც) შემდეგ filter ფუნქციის გამოყენებით
//  გადაუარეთ სიას და შეამოწმეთ თუ სახელის სიგრძე მეტი იქნება 4 ზე მაშინ დააბრუნეთ true სხვა შემთხვევაში false
var names = ["გიორგი", "ნინო", "ლევანი", "თეა", "მიხეილ", "ანა"];

function longerThan4(name) {
    return name.length > 4;
}

var longNames = names.filter(longerThan4);
console.log(longNames);


// 5) გამოიყენეთ ზემოთ შექმნილი სახელების სია თუ ელემენტი დგას კენტ ინდექსზე მაშინ დააბრუნეთ true სხვა შემთხვევაში 
// false
var names = ["გიორგი", "ნინო", "ლევანი", "თეა", "მიხეილ", "ანა"];

function isOddIndex(value, index) {
    return index % 2 === 1;
}

var indexCheck = names.map(isOddIndex);
console.log(indexCheck);


// array.find()
// დააბრუნებს პირველ ელემენტს, რომელიც აკმაყოფილებს ფუნქციას.

let nums = [1,2,3,4];
let firstEven = nums.find(n => n % 2 === 0); // 2


// array.some()
// აბრუნებს true თუ რომელიმე ელემენტი აკმაყოფილებს პირობას.

nums.some(n => n > 3); // true


// array.every()
// აბრუნებს true თუ ყველა ელემენტი აკმაყოფილებს პირობას.


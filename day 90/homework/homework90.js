// 2) JS'ში შექმენით ცვლადი სადაც შეინახავთ თქვენთვის სასურველ რიცხვს 10'ის ფარგლებში ასევე შექმენით ერთი ცვლადი სადაც მომხმარებელს შემოატანინებთ

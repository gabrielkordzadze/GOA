// დაემთხვევა თქვენს რიცხვს ალერტით გამოიტანეთ "შენ გამოიცანი ჩემი რიცხვი" თუ არა მაშინ "შენ ვერ გამოიცანი ჩემი რიცხვი"

let myNumber = 4; // ჩემი არჩეული რიცხვი

let input = document.getElementById("userGuess");
let button = document.getElementById("checkBtn");

button.addEventListener("click", function() {
    let guess = Number(input.value);
    if (guess === myNumber) {
        alert("შენ გამოიცანი ჩემი რიცხვი");
    } else {
        alert("შენ ვერ გამოიცანი ჩემი რიცხვი");
    }
});







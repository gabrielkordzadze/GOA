// 1) კომენტარის სახით ახსენით რა არის სკოუპები ვრცლად და დეტალურად ჩამოაყალიბეთ ყველაფერი თქვენივე სიტყვებით 

// სკოუპი (scope) არის კონტექსტი, სადაც ცვლადი ან ფუნქცია ხელმისაწვდომია. 
// სხვაგვარად რომ ვთქვათ, სკოუპი განსაზღვრავს "სად შეიძლება ვნახოთ და გამოვიყენოთ" კონკრეტული ცვლადი.
//
// JavaScript-ში არსებობს რამდენიმე ტიპის სკოუპი:
//
// 1) გლობალური სკოუპი (Global scope):
//    ცვლადები და ფუნქციები, რომლებიც დეკლარირებული არიან გლობალურად, ხელმისაწვდომია ნებისმიერი ადგილისთვის კოდის შიგნით.
//
// 2) ლოკალური სკოუპი (Local scope):
//    ცვლადები, რომლებიც შექმნილია ფუნქციის შიგნით, ხელმისაწვდომია მხოლოდ ამ ფუნქციის შიგნით.
//
// 3) ბლოკური სკოუპი (Block scope):
//    ცვლადები, რომლებიც შექმნილია let ან const-ის საშუალებით ბლოკში { ... }, ხელმისაწვდომია მხოლოდ იმ ბლოკის შიგნით.
//
// სკოუპი დაგვეხმარება თავიდან ავიცილოთ კონფლიქტები, მაგალითად ერთსა და იმავე სახელის მქონე ცვლადების შემთხვევაში.


// 2) შექმენი ცვლადი name გლობალურ სკოუპში და გამოიყენე ის ფუნქციის შიგნით ასევე კომენტარის სახით ახსენით რატომ შეგვიძლია 
// მისი გამოყენება ყველგან 
// გლობალური სკოუპში შექმნილი ცვლადი
var name = "Gabriel";

function greet() {
  // ფუნქციის შიგნით შეგვიძლია გამოვიყენოთ გლობალური ცვლადი
  console.log("Hello, " + name + "!"); // Hello, Gabriel!
  // შეგვიძლია გამოვიყენოთ ყველგან, რადგან name გლობალურადაა დეკლარირებული
}

greet();
console.log(name); // "Gabriel" - აქაც ვხედავთ გლობალურ ცვლადს


// 3) ფუნქციის შიგნით შექმენი ცვლადი message და სცადე მისი გამოყენება გარეთ და ასევე კომენტარის სახით ახსენით რატომ ვერ 
// ვიყენებთ მას ფუნქციის გარეთ 

function showMessage() {
  // ფუნქციის შიგნით შექმნილი ცვლადი
  var message = "This is a local message";
  console.log(message); // "This is a local message" - აქ ფუნქციაში ვხედავთ
}

showMessage();
console.log(message); 
// ❌ შეცდომა! message არ არის ხელმისაწვდომი ფუნქციის გარეთ


// 4) შექმენი ფუნქცია რომელსაც შიგნით კიდევ ერთი ფუნქცია გააჩნია და შიდა ფუნქციაში გამოიყენე გარე ფუნქციის ცვლადი 
// კომენტარის სახით ახსენით რატომ შეგვიძლია გარე ფუნქციის შიდა ფუნქციაში გამოყენება 

function outerFunction() {
  var outerVar = "I am outside";

  function innerFunction() {
    // შიდა ფუნქციაში შეგვიძლია გამოვიყენოთ გარე ფუნქციის ცვლადი
    console.log("Inner says: " + outerVar);
  }

  innerFunction();
}

outerFunction(); // Inner says: I am outside



// 1) ივარჯიშეთ for in და for of ზე და გააკეთეთ თითოეულზე 5 მაგალითი

// for in

// 1) ობიექტის გასაღებების გამოტანა
let person = {name: "გიორგი", age: 25, country: "საქართველო"};
for (let key in person) {
  console.log(key); // name, age, country
}

// 2) ობიექტის მნიშვნელობების გამოტანა
for (let key in person) {
  console.log(person[key]); // გიორგი, 25, საქართველო
}

// 3) მასივის ინდექსების გამოტანა
let fruits = ["ვაშლი", "მსხალი", "ბანანი"];
for (let index in fruits) {
  console.log(index); // 0, 1, 2
}

// 4) მასივის ინდექსებით მნიშვნელობების გამოტანა
for (let index in fruits) {
  console.log(fruits[index]); // ვაშლი, მსხალი, ბანანი
}

// 5) Nested ობიექტში გასაღებების გამოტანა
let car = {
  brand: "Toyota",
  model: "Corolla",
  details: {year: 2020, color: "შავი"}
};
for (let key in car.details) {
  console.log(key); // year, color
}



//for of

// 1) მასივის ელემენტების გამოტანა
let colors = ["წითელი", "ლურჯი", "მწვანე"];
for (let color of colors) {
  console.log(color); // წითელი, ლურჯი, მწვანე
}

// 2) სტრინგის თითო ასოს გამოტანა
let word = "გამარჯობა";
for (let letter of word) {
  console.log(letter); // გ, ა, მ, ა, რ...
}

// 3) Set-ში ელემენტების გამოტანა
let numbers = new Set([1, 2, 3]);
for (let num of numbers) {
  console.log(num); // 1, 2, 3
}

// 4) Map-ის მნიშვნელობების გამოტანა
let map = new Map([
  ["name", "ნიკა"],
  ["age", 28]
]);
for (let [key, value] of map) {
  console.log(key, value); // name ნიკა, age 28
}

// 5) მასივის ელემენტების გაორმაგება
let nums = [2, 4, 6];
for (let num of nums) {
  console.log(num * 2); // 4, 8, 12
}

